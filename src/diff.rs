use super::from_array;

pub fn diff(lhs: &Vec<Vec<i32>>, rhs:&Vec<Vec<i32>>) -> i32 {
    let mut ret = i32::MAX;

    let m = lhs.len();
    let n = lhs[0].len();

    for i in 0 .. m {
        for j in 0 .. n {
            if lhs[i][j] == rhs[i][j] {
                continue;
            }
            let val = i32::min(lhs[i][j], rhs[i][j]);
            if val < ret {
                ret = val;
            }
        }
    }

    ret
}

pub fn result(lhs: &Vec<Vec<i32>>) -> i32 {
    diff(lhs, &data())
}

